def test_wrap():
    w = TextWrapper(width=10)
    assert w.wrap("Hello, world!") == ["Hello,", "world!"]
    assert w.wrap("This is a test.") == ["This is", "a test."]
    assert w.wrap("This is a test. This is only a test.") == ["This is a", "test. This", "is only a", "test."]

def test_fill():
    w = TextWrapper(width=10)
    assert w.fill("Hello, world!") == "Hello, world!"
    assert w.fill("This is a test.") == "This is a\ntest."
    assert w.fill("This is a test. This is only a test.") == "This is a\ntest. This\nis only a\ntest."

